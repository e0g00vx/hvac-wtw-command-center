def get_store_manager(store_nbr: int) -> str:
    """Get store manager name for a store number."""
    return STORE_MANAGERS.get(store_nbr, "Unknown")


def enrich_stores_with_managers(stores: list[dict]) -> list[dict]:
    """Add store manager names to a list of store records."""
    for store in stores:
        store_nbr = store.get("store")
        if store_nbr and store_nbr in STORE_MANAGERS:
            store["store_mgr"] = STORE_MANAGERS[store_nbr]
        else:
            store["store_mgr"] = "Unknown"
    return stores
